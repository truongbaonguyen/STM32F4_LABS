/**
  * @brief  Function implementing the myTask01 thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */

	  HAL_UART_Receive_IT(&huart1, &receiveData, 1);	// Đăng ký interrupt
	  int8_t j;
	  for(;;)
	  {
		  if(rcvFlag == 1){
			  if (indeX >= 4){
				  j = 3;
				  // Gán 4 giá trị từ buffer qua ledControlData
				  for (int8_t i = indeX - 1; i >= indeX - 4; i--){
					  ledControlData[j] = buffer[i];
					  j--;
				  }
			  bufferFull = 1;
			  indeX = 0;
		      }
		   rcvFlag = 0;
	     }
		  osDelay(100);
	  }

  /* USER CODE END 5 */
}

